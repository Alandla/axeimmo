const NON_PROFESSIONAL_WEBSITES = [
  'google.com',
  'youtube.com',
  'tiktok.com',
  'instagram.com',
  'facebook.com',
  'twitter.com',
  'linkedin.com',
  'pinterest.com',
  'reddit.com',
  'snapchat.com',
  'whatsapp.com',
  'telegram.org',
  'discord.com'
];

/**
 * Determine if a user is considered professional based on their email domain and website
 * @param email User's email address
 * @param website User's website URL (optional)
 * @returns true if the user is considered professional, false otherwise
 */
export function isProfessionalUser(email: string, website?: string): boolean {
  // Extract domain from email
  const emailDomain = email.split('@')[1]?.toLowerCase();
  
  // Check if email domain is NOT a free email provider
  const hasBusinessEmail = emailDomain && !FREE_EMAIL_DOMAINS.includes(emailDomain);
  
  // Check if website is provided and is NOT a non-professional website
  const hasProfessionalWebsite = website && 
    website.trim() !== '' && 
    !NON_PROFESSIONAL_WEBSITES.some(nonprofSite => {
      const cleanWebsite = website.toLowerCase().replace(/^https?:\/\//, '').replace(/^www\./, '');
      return cleanWebsite.includes(nonprofSite);
    });
  
  // User is considered professional if they have a business email OR a professional website
  return hasBusinessEmail || Boolean(hasProfessionalWebsite);
} 